var quickSort = function (objectArray) {
    var left = [],
        right = [],
        val = objectArray[0],
        len = objectArray.length;

    if (len <= 1) {
        return objectArray;
    }

    for (var i = 1; i < len; i ++) {
        if (objectArray[i].index < val.index) {
            left.push(objectArray[i]);
        } else {
            right.push(objectArray[i]);
        }
    }
    return quickSort(left).concat(val, quickSort(right));
};


